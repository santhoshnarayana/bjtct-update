package com.bjtct.form;

public class FixturesForm {
    
    Long id;
    Long leftTeamId;
    String leftTeamName;
    Long rightTeamId;
    String rightTeamName;
    String venue;
    String date;
    public Long getId() {
        return id;
    }
    public void setId(Long id) {
        this.id = id;
    }
    public Long getLeftTeamId() {
        return leftTeamId;
    }
    public void setLeftTeamId(Long leftTeamId) {
        this.leftTeamId = leftTeamId;
    }
    public String getLeftTeamName() {
        return leftTeamName;
    }
    public void setLeftTeamName(String leftTeamName) {
        this.leftTeamName = leftTeamName;
    }
    public Long getRightTeamId() {
        return rightTeamId;
    }
    public void setRightTeamId(Long rightTeamId) {
        this.rightTeamId = rightTeamId;
    }
    public String getRightTeamName() {
        return rightTeamName;
    }
    public void setRightTeamName(String rightTeamName) {
        this.rightTeamName = rightTeamName;
    }
    public String getVenue() {
        return venue;
    }
    public void setVenue(String venue) {
        this.venue = venue;
    }
    public String getDate() {
        return date;
    }
    public void setDate(String date) {
        this.date = date;
    }
    
    

}
