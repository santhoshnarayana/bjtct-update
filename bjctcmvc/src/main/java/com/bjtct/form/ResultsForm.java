package com.bjtct.form;

public class ResultsForm {
    
    Long id;
    Long leftTeamId;
    String leftTeamName;
    Long rightTeamId;
    String rightTeamName;
    Long winningTeamId;
    String winningTeamName;
    String wonBy;
    
   
    public Long getId() {
        return id;
    }
    public void setId(Long id) {
        this.id = id;
    }
    public Long getLeftTeamId() {
        return leftTeamId;
    }
    public void setLeftTeamId(Long leftTeamId) {
        this.leftTeamId = leftTeamId;
    }
    public String getLeftTeamName() {
        return leftTeamName;
    }
    public void setLeftTeamName(String leftTeamName) {
        this.leftTeamName = leftTeamName;
    }
    public Long getRightTeamId() {
        return rightTeamId;
    }
    public void setRightTeamId(Long rightTeamId) {
        this.rightTeamId = rightTeamId;
    }
    public String getRightTeamName() {
        return rightTeamName;
    }
    public void setRightTeamName(String rightTeamName) {
        this.rightTeamName = rightTeamName;
    }
    public Long getWinningTeamId() {
        return winningTeamId;
    }
    public void setWinningTeamId(Long winningTeamId) {
        this.winningTeamId = winningTeamId;
    }
    public String getWinningTeamName() {
        return winningTeamName;
    }
    public void setWinningTeamName(String winningTeamName) {
        this.winningTeamName = winningTeamName;
    }
    public String getWonBy() {
        return wonBy;
    }
    public void setWonBy(String wonBy) {
        this.wonBy = wonBy;
    }
    
    
    

}
