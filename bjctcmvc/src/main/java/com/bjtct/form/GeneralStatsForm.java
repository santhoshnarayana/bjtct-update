package com.bjtct.form;

public class GeneralStatsForm {
    private Long id;
    private Long teamId;
    private String teamName;
    private Long played;
    private Long won;
    private Long loss;
    private Long draw;
    private Long points;
    public Long getId() {
        return id;
    }
    public void setId(Long id) {
        this.id = id;
    }
    public Long getTeamId() {
        return teamId;
    }
    public void setTeamId(Long teamId) {
        this.teamId = teamId;
    }
    public String getTeamName() {
        return teamName;
    }
    public void setTeamName(String teamName) {
        this.teamName = teamName;
    }
    public Long getPlayed() {
        return played;
    }
    public void setPlayed(Long played) {
        this.played = played;
    }
    public Long getWon() {
        return won;
    }
    public void setWon(Long won) {
        this.won = won;
    }
    public Long getLoss() {
        return loss;
    }
    public void setLoss(Long loss) {
        this.loss = loss;
    }
    public Long getDraw() {
        return draw;
    }
    public void setDraw(Long draw) {
        this.draw = draw;
    }
    public Long getPoints() {
        return points;
    }
    public void setPoints(Long points) {
        this.points = points;
    }
    
    

}
